let firstFavNum = 3;
let secondFavNum = 9;

// Addition
let sum = firstFavNum + secondFavNum;
console.log("Sum:", sum);

// Subtraction
let difference = firstFavNum - secondFavNum;
console.log("Difference:", difference);

// Multiplication
let product = firstFavNum * secondFavNum;
console.log("Product:", product);

// Division
let division = firstFavNum / secondFavNum;
console.log("Division:", division);

// Modulus
let modulus = firstFavNum % secondFavNum;
console.log("Modulus:", modulus);

// Power
let power = firstFavNum ** 1;
console.log("Power:", power);
